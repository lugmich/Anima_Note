import UIKit

 
func displayCMajScale(on theSegue: PianoViewController  ) {

  let display = theSegue
   
   display.labelOfTheI = MajorScale.C().tonic
   display.labelOfTheII = MajorScale.C().supertonic
   display.labelOfTheIII = MajorScale.C().mediant
   display.labelOfTheIV = MajorScale.C().subdominant
   display.labelOfTheV = MajorScale.C().dominant
   display.labelOfTheVI = MajorScale.C().submediant
   display.labelOfTheVII = MajorScale.C().leadingtone
}

